    private void GenRepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GenRepActionPerformed
        
        JFileChooser fc = new JFileChooser();
        fc.setSelectedFile(new File("Reporte_Sugar_" + 
            new SimpleDateFormat("ddMMyyyy_HHmm").format(new Date()) + ".pdf"));

        if (fc.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
            try (FileOutputStream fos = new FileOutputStream(fc.getSelectedFile())) {

                String rutaLogo = "/recursos/logo_sugar.png";  // CLASSPATH

                ReportePDF.generar(fos, rutaLogo);
                JOptionPane.showMessageDialog(this, "Reporte generado:\n" + fc.getSelectedFile().getName());

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
                e.printStackTrace();
            }
    
}
    }//GEN-LAST:event_GenRepActionPerformed

   private void configurarTabla() {
        jTable1.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int row = jTable1.getSelectedRow();
                if (row >= 0) {
                    // ASIGNAR EN ORDEN CRUZADO (según layout)
                    NomTxt.setText(jTable1.getValueAt(row, 1).toString());           // Nombre
                    StockTxt.setText(jTable1.getValueAt(row, 2).toString());         // Precio → va a StockTxt
                    BarrasTxt.setText(jTable1.getValueAt(row, 3).toString());        // Stock → va a BarrasTxt
                    PreTxt.setText(jTable1.getValueAt(row, 4).toString());           // Código de barras → va a PreTxt
                    DesTxt.setText(jTable1.getValueAt(row, 5).toString());           // Descripción

                    // Deshabilitar botón
                    BtnAgregar.setEnabled(false);
                    BtnAgregar.setText("Selección activa");

                    // Doble clic = limpiar
                    if (evt.getClickCount() == 2) {
                        limpiarCampos();
                    }
                }
            }
        });

        // ESC = limpiar
        jTable1.addKeyListener(new java.awt.event.KeyAdapter() {
            @Override
            public void keyPressed(java.awt.event.KeyEvent evt) {
                if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_ESCAPE) {
                    limpiarCampos();
                }
            }
        });
    }
    
    
    private void cargarTabla() {
        DefaultTableModel modelo = new DefaultTableModel(
            new Object[][] {},
            new String[] {"ID", "Nombre", "Precio", "Stock", "Código de Barras", "Descripción"}
        ) {
            @Override
            public boolean isCellEditable(int row, int column) { return false; }
        };

        try (Connection c = conexionBD.ConexionBD.conectar();
             Statement st = c.createStatement();
             ResultSet rs = st.executeQuery("SELECT * FROM productos")) {

            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getInt("codigoProducto"),
                    rs.getString("nombreProducto"),
                    rs.getDouble("precio"),
                    rs.getInt("cantidad"),
                    rs.getInt("codigoDeBarras"),
                    rs.getString("descripcion")
                });
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al cargar: " + e.getMessage());
        }

        jTable1.setModel(modelo);
    }
    
    private void actualizarProducto(int id) {
        String nombre = NomTxt.getText().trim();
        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El nombre es obligatorio.");
            return;
        }

        // ORDEN CORREGIDO
        double precio = Validaciones.veriDoublePositivo(StockTxt.getText().trim(), "Precio");
        int stock = Validaciones.veriEnteroPositivo(BarrasTxt.getText().trim(), "Stock");
        int barras = 0;
        String barrasStr = PreTxt.getText().trim();
        if (!barrasStr.isEmpty()) {
            barras = Validaciones.veriEnteroPositivo(barrasStr, "Código de barras");
            if (barras <= 0) return;
        }
        String desc = DesTxt.getText().trim();

        Producto p = new Producto(id, nombre, precio, stock, barras, desc);
        if (ControladorProducto.actualizar(p)) {
            limpiarCampos();
            cargarTabla();
        }
    }
    
    private void eliminarProducto(int id) {
        int confirm = JOptionPane.showConfirmDialog(this,
            "¿Eliminar producto ID " + id + "?",
            "Confirmar", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            if (ControladorProducto.eliminar(id)) {
                limpiarCampos();
                cargarTabla();
            }
        }
    }

