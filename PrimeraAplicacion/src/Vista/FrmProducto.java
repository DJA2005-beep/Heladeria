    private void BtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAgregarActionPerformed
        // === 1. BtnAgregarActionPerformed() ===

            int row = jTable1.getSelectedRow();
            if (row != -1) {
                JOptionPane.showMessageDialog(this,
                    "Para agregar un NUEVO producto, primero LIMPIE los campos o presione ESC.",
                    "Advertencia", JOptionPane.WARNING_MESSAGE);
                return;
            }

            String nombre = NomTxt.getText().trim();
            if (nombre.isEmpty()) {
                JOptionPane.showMessageDialog(this, "El nombre es obligatorio.");
                return;
            }

            // ORDEN CORREGIDO SEGÚN EL LAYOUT REAL
            double precio = Validaciones.veriDoublePositivo(StockTxt.getText().trim(), "Precio");     // ← Precio está en StockTxt
            int stock = Validaciones.veriEnteroPositivo(BarrasTxt.getText().trim(), "Stock");         // ← Stock está en BarrasTxt
            int barras = 0;
            String barrasStr = PreTxt.getText().trim();  // ← Código de barras está en PreTxt
            if (!barrasStr.isEmpty()) {
                barras = Validaciones.veriEnteroPositivo(barrasStr, "Código de barras");
                if (barras <= 0) return;
            }
            String desc = DesTxt.getText().trim();

            Producto p = new Producto(0, nombre, precio, stock, barras, desc);
            if (ControladorProducto.crear(p)) {
                limpiarCampos();
                cargarTabla();
            }

    }//GEN-LAST:event_BtnAgregarActionPerformed

